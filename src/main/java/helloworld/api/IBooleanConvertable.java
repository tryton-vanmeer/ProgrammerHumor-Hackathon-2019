package helloworld.api;

/**
 * IBooleanConvertable
 * 
 * Classes that implement IBooleanConvertable can be repersented as a Boolean.
 */
public interface IBooleanConvertable
{
    /**
     * Returns a boolean representation of the object.
     * 
     * @return a boolean representation of the object
     */
    public boolean toBoolean();
}